Record cons(int a, const char *b, int c, float d) {
    Record tmpr;
    Value tmp;
    tmp.type = Type::INT;
    tmp.ptr = new int(a);
    tmpr.push_back(tmp);
    tmp.type = Type::CHAR;
    tmp.clen = strlen(b) + 1;
    tmp.ptr = new char[tmp.clen];
    strcpy((char *) tmp.ptr, b);
    tmpr.push_back(tmp);
    tmp.type = Type::INT;
    tmp.ptr = new int(c);
    tmpr.push_back(tmp);
    tmp.type = Type::FLOAT;
    tmp.ptr = new float(d);
    tmpr.push_back(tmp);
    return tmpr;
}

